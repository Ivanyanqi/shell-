#!/bin/bash
#定义函数
func(){
    echo $1 ,$2 #访问参数1和参数2
    echo $@ #已列表的方式一次答应所有参数
    echo "$*" #类似于$@，但是参数被作为单个实体，要比"$*"用得多。由于 "$*"将所有的参数当做单个字符串，因此它很少被使用
    return 0 # 返回值
}
#$* 和 $@ 都表示传递给函数或脚本的所有参数，不被双引号(" ")包含时，都以"$1" "$2" … "$n" 的形式输出所有参数。
#但是当它们被双引号(" ")包含时，"$*" 会将所有的参数作为一个整体，以"$1 $2 … $n"的形式输出所有参数；
#"$@" 会将各个参数分开，以"$1" "$2" … "$n" 的形式输出所有参数

#调用函数
#不穿参
func
#传参
func hello world
#参数里有空，加引号
func 'hello world'